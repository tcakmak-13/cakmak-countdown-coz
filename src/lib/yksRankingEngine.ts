// ─── Hesaplama Motoru ───────────────────────────────────────────────────

interface RankingResult {
  bestEstimate: number;
  lowerBound: number;
  upperBound: number;
  confidence: number; // 0-100 arası güvenilirlik
}

/** Tek yıl verisi için interpolasyon */
function interpolateYear(data: YearData, score: number): number | null {
  if (data.length === 0) return null;

  // Puanlar azalan sırada (yüksek puan = düşük sıralama)
  // Score en yüksek puan değerinden büyükse
  if (score >= data[0][0]) return data[0][1];
  // Score en düşük puan değerinden küçükse
  if (score <= data[data.length - 1][0]) return data[data.length - 1][1];

  // İki nokta arası lineer interpolasyon
  for (let i = 0; i < data.length - 1; i++) {
    const [p1, r1] = data[i];
    const [p2, r2] = data[i + 1];
    if (score <= p1 && score >= p2) {
      const ratio = (p1 - score) / (p1 - p2);
      // Logaritmik interpolasyon (sıralamalar logaritmik dağılım gösterir)
      const logR1 = Math.log(r1);
      const logR2 = Math.log(r2);
      return Math.round(Math.exp(logR1 + ratio * (logR2 - logR1)));
    }
  }
  return null;
}

/** Zorluk düzeltme katsayıları (yıllar arası normalize etmek için) */
const DIFFICULTY_FACTORS: Record<string, Record<number, number>> = {
  TYT: { 2023: 1.00, 2024: 1.02, 2025: 1.01 },
  SAY: { 2023: 1.00, 2024: 1.03, 2025: 1.015 },
  EA:  { 2023: 1.00, 2024: 1.02, 2025: 1.01 },
  SOZ: { 2023: 1.00, 2024: 1.025, 2025: 1.012 },
};

/** 3 yılın ortalaması + standart sapma ile hassas tahmin */
export function estimateRanking(score: number, type: 'TYT' | 'SAY' | 'EA' | 'SOZ'): RankingResult {
  const datasets: Record<string, YearData[]> = {
    TYT: [TYT_2023, TYT_2024, TYT_2025],
    SAY: [SAY_2023, SAY_2024, SAY_2025],
    EA: [EA_2023, EA_2024, EA_2025],
    SOZ: [SOZ_2023, SOZ_2024, SOZ_2025],
  };

  const years = datasets[type];
  const factors = DIFFICULTY_FACTORS[type];
  const yearKeys = [2023, 2024, 2025];

  const estimates: number[] = [];
  for (let i = 0; i < years.length; i++) {
    const raw = interpolateYear(years[i], score);
    if (raw !== null) {
      // Zorluk düzeltmesi uygula (en son yıla normalize et)
      const factor = factors[yearKeys[i]] / factors[2025];
      estimates.push(Math.round(raw * factor));
    }
  }

  if (estimates.length === 0) {
    return { bestEstimate: 0, lowerBound: 0, upperBound: 0, confidence: 0 };
  }

  // Ağırlıklı ortalama (en son yıla daha fazla ağırlık)
  const weights = [0.2, 0.3, 0.5]; // 2023, 2024, 2025
  let weightedSum = 0;
  let weightTotal = 0;
  for (let i = 0; i < estimates.length; i++) {
    weightedSum += estimates[i] * weights[i];
    weightTotal += weights[i];
  }
  const bestEstimate = Math.round(weightedSum / weightTotal);

  // Standart sapma hesapla
  const mean = estimates.reduce((a, b) => a + b, 0) / estimates.length;
  const variance = estimates.reduce((sum, val) => sum + Math.pow(val - mean, 2), 0) / estimates.length;
  const stdDev = Math.sqrt(variance);

  // Hata payı: %5-8 arası (puana göre dinamik)
  // Yüksek puanlarda daha hassas, düşük puanlarda daha geniş
  const errorMultiplier = score >= 400 ? 0.05 : score >= 300 ? 0.06 : score >= 200 ? 0.07 : 0.08;
  const naturalError = Math.max(stdDev * 1.2, bestEstimate * errorMultiplier * 0.3);

  const lowerBound = Math.max(1, Math.round(bestEstimate - naturalError));
  const upperBound = Math.round(bestEstimate + naturalError);

  // Güvenilirlik: Veri noktalarına yakınlık ve standart sapmanın düşüklüğü
  const cvRatio = mean > 0 ? stdDev / mean : 1;
  const confidence = Math.min(95, Math.max(60, Math.round(95 - cvRatio * 200)));

  return { bestEstimate, lowerBound, upperBound, confidence };
}

/** Sıralamayı okunabilir formata çevir */
export function formatRanking(n: number): string {
  if (n <= 0) return '-';
  if (n >= 1000000) {
    return (n / 1000000).toFixed(2).replace(/\.?0+$/, '') + 'M';
  }
  if (n >= 1000) {
    return n.toLocaleString('tr-TR');
  }
  return n.toString();
}

/** Kısa format (kartlar için) */
export function formatRankingShort(n: number): string {
  if (n <= 0) return '-';
  if (n >= 1000000) return (n / 1000000).toFixed(1) + 'M';
  if (n >= 100000) return Math.round(n / 1000) + 'K';
  if (n >= 10000) return (n / 1000).toFixed(1) + 'K';
  if (n >= 1000) return (n / 1000).toFixed(1) + 'K';
  return n.toString();
}
